package inheritance;

public class Employee {
	private String name;

	// Constructors
	public Employee() {

	}

	Employee(String name) {
		this.name = name;
	}

	//Getter and setter methods
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getName() {
		return name;
	}

	

}
